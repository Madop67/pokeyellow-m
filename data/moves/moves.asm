	      ; into wPlayerMoveNum/wEnemyMoveNum by ReadMove. The animation a
	      ; move actually plays is its AttackAnimationPointers row.)
	db \2 ; effect
	db \3 ; power
	db \4 ; type
	db \5 percent ; accuracy
	db \6 ; pp
	ASSERT \6 <= 40, "PP must be 40 or less"
ENDM

